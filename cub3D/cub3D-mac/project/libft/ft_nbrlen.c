#include "libft.h"

size_t		ft_nbrlen(int nbr)
{
	size_t	len;

	len = 0;
	if (nbr == 0)
		return (1);
	if (nbr < 0)
		len += 1;
	while (nbr)
	{
		nbr /= 10;
		len += 1;
	}
	return (len);
}
